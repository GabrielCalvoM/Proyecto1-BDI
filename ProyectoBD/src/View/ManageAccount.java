    private void ViewProductBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewProductBtnActionPerformed
        int index = favoritesList.getSelectedIndex();
        if (index < 0) {
            mainFrame.showError("Debe seleccionar un elemento.");
            return;
        }
        ListModel model = favoritesList.getModel();
        Product product = (Product) model.getElementAt(index);
        mainFrame.showPage("ViewMovie", new ViewMovie(mainFrame, this, product));
    }//GEN-LAST:event_ViewProductBtnActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (mainFrame.userAccount != null && mainFrame.userAccount.getId_accountType() != 1) {
            mainFrame.showError("No tiene permisos de administrador");
            return;
        }
        System.out.println(mainFrame.userAccount.getId_accountType());
        mainFrame.showPage("AdminPage", new AdminPage(mainFrame, this));
    }//GEN-LAST:event_jButton2ActionPerformed


