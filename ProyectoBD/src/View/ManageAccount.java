    private void favs_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_favs_buttonActionPerformed
        try {
            int idWishlist = Cursors.getWishlistId(mainFrame.userAccount.getId_user());
            DefaultListModel model = mainFrame.buildListModel(Cursors.getProductsInWishlist(idWishlist));
            favoritesList.setModel(model);
        }
        catch (Exception e) {
            mainFrame.showError("Error al obtener favoritos.");
            return;
        }
        CardLayout card = (CardLayout) this.getLayout();
        card.show(this, "Favorites");
    }//GEN-LAST:event_favs_buttonActionPerformed

