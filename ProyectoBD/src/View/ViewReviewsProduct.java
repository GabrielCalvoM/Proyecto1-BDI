    public void loadData() {
        ArrayList<Review> reviews;
        movieNameTxt.setText(product.getTitle());
        try {
            reviews = Cursors.getProductReviews(product.getId());
        }
        catch (Exception e) {
            mainFrame.showError("Error al cargar reviews.");
            return;
        }
        DefaultTableModel reviewModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        String[] columns = {"Usuario", "Puntuación"};
        reviewModel.setColumnIdentifiers(columns);
        
        try {
            for (Review r : reviews) {
                Object[] row = new Object[2];
                row[0] = Cursors.getUsernameByUserId(r.getId_user());
                row[1] = r;
                reviewModel.addRow(row);
                
                if (mainFrame.userAccount != null && r.getId_user() == mainFrame.userAccount.getId_user()) {
                    addButton.setText("Actualizar");
                    newReviewTxt.setText(r.getDescription());
                    newReviewRating.setSelectedIndex(5 - r.getScore());
                    rated = true;
                    userReviewId = r.getId();
                }
            }
        }
        catch (Exception e) {
            mainFrame.showError("Error al cargar las reviews");
        }
        reviewTable.setModel(reviewModel);
        
        if (!rated) {
            removeButton.setEnabled(false);
        }
        ListSelectionModel selectionModel = reviewTable.getSelectionModel();
        selectionModel.addListSelectionListener(new ListSelectionListener() {
        public void valueChanged(ListSelectionEvent e) {
            if (reviewTable.getSelectedRow() == -1) {
                reviewTable.setRowSelectionInterval(0, 0);
            }
            if (!e.getValueIsAdjusting()) {
                TableModel model = reviewTable.getModel();
                int index = reviewTable.getSelectedRow();
                Review review = (Review) model.getValueAt(index, 1);
                selectedReviewTxt.setText(review.getDescription());
            }
        }
        });
    }

