    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (mainFrame.userAccount == null) {
            mainFrame.showPage("SignInPage", new SignInPage(mainFrame, this));
            return;
        }
        String comment = newReviewTxt.getText();
        System.out.println(newReviewRating.getSelectedItem());
        int rating = Integer.parseInt(newReviewRating.getSelectedItem().toString());
        int idUser = mainFrame.userAccount.getId_user();
        int idProduct = product.getId();
        if (!rated)
            try {
                Insertions.insertReview(comment, rating, idUser, idProduct);
            }
            catch (Exception e) {
                mainFrame.showError("Error al agregar comentario.");
                return;
            }
        else {
            try {
                Deletions.deleteReview(userReviewId);
                Insertions.insertReview(comment, rating, idUser, idProduct);
            }
            catch (Exception e) {
                mainFrame.showError("Error al actualizar la review.");
                return;
            }
        }
        loadData();
        addButton.setText("Actualizar");
        removeButton.setEnabled(true);
    }//GEN-LAST:event_addButtonActionPerformed

